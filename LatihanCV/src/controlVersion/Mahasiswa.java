package controlVersion;

public class Mahasiswa {
	
	private String nrp;
    private String nama;
    private double ipk;
    
    public String getNrp(){
        return nrp;
    }
    
    public void setNrp(String nrp){
        this.nrp = nrp;
    }
    
    public String getNama(){
        return nama;
    }
    
    public void setNama(String nama){
        this.nama = nama;
    }
    
    public double getIpk(){
        return ipk;
    }
    
    public void setIpk(double ipk){
        this.ipk = ipk;
    }

}
